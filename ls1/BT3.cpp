
// Lưu trữ danh sách sinh viên theo thứ tự nhập vào
// Cấu trúc dữ liệu: Danh sách liên kết (LinkedList)
// Lý do: Danh sách liên kết dễ dàng thêm các phần tử mới theo thứ tự nhập vào mà không cần sắp xếp lại. Nó tiết kiệm thời gian nếu không cần truy cập ngẫu nhiên.

// Tìm kiếm nhanh tên một sản phẩm trong danh sách hàng triệu sản phẩm
// Cấu trúc dữ liệu: Cây tìm kiếm nhị phân (Binary Search Tree) hoặc Hash Table
// Lý do: Với Binary Search Tree (hoặc cấu trúc như AVL, Red-Black Tree), thời gian tìm kiếm được giảm xuống O(log n). Hash Table cung cấp thời gian truy cập trung bình O(1).

// Quản lý danh sách các task công việc cần xử lý theo thứ tự ưu tiên
// Cấu trúc dữ liệu: Hàng đợi ưu tiên (Priority Queue) hoặc Heap
// Lý do: Hàng đợi ưu tiên hoặc Heap cho phép dễ dàng quản lý thứ tự ưu tiên của các task, giúp chọn task có ưu tiên cao nhất để xử lý.

// Lưu trữ dữ liệu hệ thống phân cấp như thư mục máy tính
// Cấu trúc dữ liệu: Cây (Tree)
// Lý do: Cây phản ánh chính xác cấu trúc phân cấp, nơi một thư mục mẹ có thể chứa nhiều thư mục con.

// Tìm đường đi ngắn nhất giữa 2 điểm trên bản đồ
// Cấu trúc dữ liệu: Đồ thị (Graph)
// Lý do: Đồ thị biểu diễn các điểm và các con đường (cạnh) giữa chúng. Các thuật toán như Dijkstra hoặc A* có thể được áp dụng để tìm đường đi ngắn nhất.